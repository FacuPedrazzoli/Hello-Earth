
### Operadores aritmeticos ###

print(3 + 4)    # Operador de suma
print(3 - 4)    # Operador de resta
print(3 * 4)    # Operador de multiplicacion
print(3 / 4)    # Operador de division
print(3 ** 4)   # Operador de potencia
print(10 % 3)   # Operador de modulo
print(10 // 3)  # Operador de division sin comas

print("Hola " + "Python. " + "Como va??")
print("Hola " + str(8))
print("Hola " * 8)

my_float = 2.5 * 2
print("Hola " * int(my_float))


print("\n")
### Operadores comparativos ###

print(3 < 4)    # Operador de menor
print(3 > 4)    # Operador de mayor
print(3 <= 4)   # Operador de menor o igual
print(3 >= 4)   # Operador de mayor o igual
print(3 == 4)   # Operador de igualdad
print(3 != 4)   # Operador de diferencia

print("\n")

print("Hola" < "Python")
print("Hola" > "Python")    
print("Hola" <= "Python")    
print("Hola" >= "Zola")              # Ordenacion alfabetica
print(len("AAAA") >= len("oooo"))    # Cuenta los caracteres
print("Hola" == "Python")    
print("Hola" != "Python")

print("\n")

### Operadores logicos ###

print(3 > 4 and "Hola" > "Python")  
print(3 > 4 or "Hola" > "Python")   
print(3 < 4 and "Hola" < "Python")  
print(3 < 4 or "Hola" > "Python")   

print(not(3 > 4))
